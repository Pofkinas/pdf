void PDF_Delay (uint32_t milliseconds) {
    if (milliseconds == 0) {
        return;
    }

    uint32_t iterations = ((SYSTEM_CLOCK_HZ / 1000) * milliseconds) / TICKS_PER_LOOP;
    volatile uint32_t tick;
    
    for (tick = 0; tick < iterations; tick++) {}

    return;
}
