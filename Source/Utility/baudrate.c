const uint32_t Baudrate_GetValue (const eBaudrate_t baudrate) {
    if ((baudrate <= eBaudrate_First) || (baudrate >= eBaudrate_Last)) {
        return 0;
    }

    return g_static_baudrate_lut[baudrate];
}
