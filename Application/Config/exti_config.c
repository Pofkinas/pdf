bool Exti_Config_IsCorrectExti (const eExti_t exti) {
    return (exti >= eExti_First) && (exti < eExti_Last);
}

const sExtiDesc_t *Exti_Config_GetExtiDesc (const eExti_t exti) {
    if (!Exti_Config_IsCorrectExti(exti)) {
        return NULL;
    }

    return &g_static_exti_lut[exti];
}
