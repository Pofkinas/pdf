static const uint8_t g_lcd_1_row_addresses[] = {FIRST_ROW_ADDRESS, SECOND_ROW_ADDRESS};

/* clang-format off */
static const sLcdDesc_t g_static_lcd_lut[eLcd_Last] = {
    [eLcd_1] = {
        .i2c = I2C_1,
        .i2c_address = LCD_1_ADDRESS,
        .rows = eLcdRow_2,
        .columns = eLcdColumn_16,
        .row_addresses = g_lcd_1_row_addresses
    },
};
