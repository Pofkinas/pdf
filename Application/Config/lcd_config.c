bool LCD_Config_IsCorrectLcd (const eLcd_t lcd_device) {
    return (lcd_device >= eLcd_First) && (lcd_device < eLcd_Last);
}

const sLcdDesc_t *LCD_Config_GetLcdDesc (const eLcd_t lcd_device) {
    if (!LCD_Config_IsCorrectLcd(lcd_device)) {
        return NULL;
    }

    return &g_static_lcd_lut[lcd_device];
}